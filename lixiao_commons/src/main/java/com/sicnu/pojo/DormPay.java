package com.sicnu.pojo;


public class DormPay {


  private String dormpayID;

  private String stuID;

  private String detail;

  private double pay;


  public String getDormpayID() {
    return dormpayID;
  }

  public void setDormpayID(String dormpayID) {
    this.dormpayID = dormpayID;
  }


  public String getStuID() {
    return stuID;
  }

  public void setStuID(String stuID) {
    this.stuID = stuID;
  }


  public String getDetail() {
    return detail;
  }

  public void setDetail(String detail) {
    this.detail = detail;
  }


  public double getPay() {
    return pay;
  }

  public void setPay(double pay) {
    this.pay = pay;
  }

}
